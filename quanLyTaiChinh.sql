USE  QuanLyTaiChinhCaNhan
GO

CREATE TABLE USERS 
(
STT INT IDENTITY (1,1) PRIMARY KEY,
FullName NVARCHAR(100) NOT NULL,
DOB DATE NULL,
SDT VARCHAR(11) NULL,
GTinh NVARCHAR (7) NULL,
BANK NVARCHAR(100) NULL,
STK VARCHAR(13) NULL
)
GO

CREATE TABLE THU
(
STT INT IDENTITY NOT NULL  FOREIGN KEY(STT) REFERENCES USERS(STT),
THANG int NOT NULL PRIMARY KEY,
LUONG MONEY NULL,
PHUCAP MONEY NULL,
THUONG MONEY NULL,
DAUTU MONEY NULL,
LAI MONEY NULL
)
GO

CREATE TABLE CHI
(
STT INT IDENTITY NOT NULL FOREIGN KEY(STT) REFERENCES USERS(STT),
THANG INT NOT NULL PRIMARY KEY CONSTRAINT FK_CHI_THANG FOREIGN KEY (THANG)  REFERENCES THU(THANG),
DIENNUOC money NULL,
NHA MONEY NULL,
ANUONG MONEY NULL,
DILAI MONEY NULL,
YTE MONEY NULL,
LIENLAC MONEY NULL,
KHAC MONEY NULL,
)
GO

